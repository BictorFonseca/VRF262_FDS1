#Conversions for HW

def miles_to_km(miles):
    return miles * 1.609
def km_to_miles(km):
    return km * 0.6214
def fah_to_cel(f):
    return ((f-32)*(5/9))
def cel_to_fah(c):
    return (c *(9/5)) + 32
def kilo_to_pound(kg):
    return kg * 2.205
def pound_to_kilo(lb):
    return lb * 0.4536


